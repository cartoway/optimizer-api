class FirstSolType
  def type_cast(value)
    if value
      if value.is_a?(String)
        value.delete!(' ') if value.include?(' ')
        value.split(',')
      elsif value.is_a?(Array)
        value
      else
        raise ArgumentError, 'Invalid first_solution_value value'
      end
    end
  end
end
